    public void Loadempno(){
        
        try {
            pst = con.prepareStatement("SELECT empno,department FROM employee");
            rs = pst.executeQuery();   
            txtempno.removeAllItems();
            txtdetype.removeAllItems();
            
            while(rs.next()){
                txtempno.addItem(rs.getString(1));
                txtdetype.addItem(rs.getString(2));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Allowance.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void table_data(){
        
        int q;
        
        try {
            pst = con.prepareStatement("SELECT * FROM allowance");
            rs = pst.executeQuery();
            
            ResultSetMetaData rss = (ResultSetMetaData) rs.getMetaData();
            q = rss.getColumnCount();
            
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            DefaultTableModel df1 = (DefaultTableModel)jTable2.getModel();
            DefaultTableModel df2 = (DefaultTableModel)jTable3.getModel();
            df.setRowCount(0);
            df1.setRowCount(1);
            df2.setRowCount(2);
            
            while(rs.next()){
                
                Vector v2 = new Vector();
                for(int a=1;a<=q;a++){
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("allowance_type"));
                    v2.add(rs.getString("amount"));
                    v2.add(rs.getString("empno"));
                }
                
                df.addRow(v2);
                 df1.addRow(v2);
                 df2.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Allowance.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

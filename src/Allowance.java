    private void btnSaveDepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveDepActionPerformed
        // TODO add your handling code here:

        String depart = txtdetype.getSelectedItem().toString();
        String allow = txtalloDep.getSelectedItem().toString();
        String amount = txtamountDep.getText();

        try {
            pst = con.prepareStatement("SELECT empno FROM employee WHERE department =?");
            pst.setString(1,depart);
            rs = pst.executeQuery();

            String empvalue;
            while(rs.next()){

                empvalue = rs.getString("empno");
                pst = con.prepareStatement("INSERT INTO allowance(allowance_type,amount,empno)VALUES(?,?,?)");
                pst.setString(1,allow);
                pst.setString(2, amount);
                pst.setString(3, empvalue);
                pst.executeUpdate();
            }

            JOptionPane.showMessageDialog(this,"Successfully added to department");
            txtamount.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(Allowance.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnSaveDepActionPerformed

